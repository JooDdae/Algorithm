#include<bits/stdc++.h>
using namespace std;
int n;
char s[101010];
char num[10][10][10]={
	{"###","#.#","#.#","#.#","###"},
	{"###","###","###","###","###"},
	{"###","..#","###","#..","###"},
	{"###","..#","###","..#","###"},
	{"#.#","#.#","###","..#","..#"},
	{"###","#..","###","..#","###"},
	{"###","#..","###","#.#","###"},
	{"###","..#","..#","..#","..#"},
	{"###","#.#","###","#.#","###"},
	{"###","#.#","###","..#","###"}
};
int main(){
    scanf("%d",&n);
    scanf("%s",s+1);
    n/=5;
    for(int i=1;i<=n;i++){
    	if(s[i] == '#'){
    		if(s[i+n] == '#' && s[i+n*3] == '#' && (s[i+1] == '.' || i==n)){
    			printf("1");
    			continue;
    		}
    		for(int j=0;j<=9;j++){
    			int fl=0;
    			for(int k=0;k<5;k++) for(int l=0;l<3;l++) if(num[j][k][l] != s[i+k*n+l]) fl=1;
    			if(!fl){
    				printf("%d",j);
    				break;
    			}
    		}
    		i+=2;
    	}
    }
}